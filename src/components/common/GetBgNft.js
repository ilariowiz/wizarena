const getBgNft = (bgName) => {

    if (bgName === "Sunrise") {
        return "#c8c6a5"
    }
    if (bgName === "Sky") {
        return "#d2eaef"
    }
    if (bgName === "Dark") {
        return "#222034"
    }
    if (bgName === "Forest") {
        return "#0f2000"
    }
    if (bgName === "Hell") {
        return "#570101"
    }
    if (bgName === "Ice") {
        return "#eef9fc"
    }
    if (bgName === "Swamp") {
        return "#333332"
    }
    if (bgName === "Deep Forest") {
        return "#141a0f"
    }
    if (bgName === "Desert") {
        return "#ad8155"
    }
    if (bgName === "Tempest") {
        return "#767b84"
    }
    if (bgName === "Dark Red") {
        return "#370000"
    }
    if (bgName === "Dark Purple") {
        return "#392232"
    }
    if (bgName === "Cave") {
        return "#7f6250"
    }
    if (bgName === "Storm") {
        return "#1e2b42"
    }
}

export default getBgNft
